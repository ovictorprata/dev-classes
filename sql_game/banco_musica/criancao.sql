CREATE TABLE artistas (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nome VARCHAR(255) NOT NULL,
    genero VARCHAR(100),
    pais_origem VARCHAR(100),
    data_nascimento DATE
);


CREATE TABLE musicas (
    id INT AUTO_INCREMENT PRIMARY KEY,
    nome VARCHAR(255) NOT NULL,
    ano_lancamento INT,
    id_artista INT,
    duracao TIME,
    FOREIGN KEY (id_artista) REFERENCES artistas(id)
);


